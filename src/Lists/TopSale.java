
package Lists;

public class TopSale {
    String id,name,topSale;
    public TopSale(String id,String name,String topSale){
        this.id=id;
        this.name=name;
        this.topSale=topSale;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTopSale() {
        return topSale;
    }

    public void setTopSale(String topSale) {
        this.topSale = topSale;
    }
    
}
